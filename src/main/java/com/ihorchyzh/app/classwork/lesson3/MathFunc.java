public class MathFunc {

    public int multiply(int a, int b){

        return a * b;

    }

    public int sum (int a, int b){

        return a + b;

    }

    public int diff (int a, int b){

        return a - b;

    }

    public int div (int a, int b){

        return a / b;

    }

}